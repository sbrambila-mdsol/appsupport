  Function SendSlackMsg([string] $Channel,[string] $Body_Text, $Success)  
  { 
      if($Success) {
        $emoji = ":heavy_check_mark:" 
        $bodytxt = "$Body_Text"
        $slackuser = "$Customer - Strata Prod AdHoc Checker Success"
      } else {
        $emoji = ":autobot:"
        $bodytxt = "<!here>  $Body_Text"
        $slackuser = "$Customer - Strata Prod AdHoc Checker Failure"
      }


      $payload = @{
	    "channel" = "#prod_adhoc_bot"
	    "icon_emoji" = $emoji
	    "username" = $slackuser
	    "text" = "$bodytxt"
      }

      Invoke-WebRequest -UseBasicParsing -Uri "https://hooks.slack.com/services/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" -Method Post -Body (ConvertTo-Json -Compress -InputObject $payload)
  } 

  Function PopSQLEventLog([string] $ServerName, [string] $DbName, [string] $ErrorLogMsg)  
  { 
    $AdminConnStr = "Data Source=$ServerName;Initial Catalog=$DbName; Integrated Security=true;"
    $AdminConn  = New-Object System.Data.SqlClient.SQLConnection($AdminConnStr)   
    
    $ErrorMsgOut = "$ErrorLogMsg"  
    Write-Host $ErrorMsgOut

    
    $EventLogCmdText = "EXEC xp_logevent 60000, '$ErrorMsgOut', ERROR"    
    $SqlCommand1 = New-Object system.Data.SqlClient.SqlCommand($EventLogCmdText, $AdminConn)   
    Try 
    {
        
        $AdminConn.Open() 
        $SqlCommand1.ExecuteNonQuery() | Out-Null 
    }
    Catch [System.Exception] 
    {
      $ex = $_.Exception  
      Write-Host $ex.Message 
    }
    Finally 
    { 
      $AdminConn.Close() 
      $AdminConn.Dispose()   
    }  
  }  

 
  $SuccessPrefix = "$SrcServer - STRATA AD HOC Database check successful! "
  $ErrorPrefix = "$SrcServer - STRATA AD HOC Database check fails! "

  $SrcConnStr = ConnectionString $SrcServer $SrcDatabase $SrcUsername $SrcPwd
  $SrcConn  = New-Object System.Data.SqlClient.SQLConnection($SrcConnStr)  
  $CmdText = "SELECT COUNT(1) FROM " + $SrcTable 
  $SqlCommand = New-Object system.Data.SqlClient.SqlCommand($CmdText, $SrcConn)  
  [string] $EventLogMsg = ""

 
  Try 
  { 
    $SrcConn.Open() 
    Write-Host "Table $SrcTable in $SrcDatabase login check is successful."
    
    $Rows = [Int32]$SqlCommand.ExecuteScalar() 
    if($Rows -eq 0) {
      $EventLogMsg = "Table $SrcTable in $SrcDatabase database with 0 records."   
      PopSQLEventLog $SrcServer $SrcDatabase "$ErrorPrefix  $EventLogMsg"
      SendSlackMsg "#applicationsupport_status" "$ErrorPrefix  $EventLogMsg" $FALSE 

      $SrcConn.Close() 
      
    } else {
        $SuccessRowCheckMsg = "Table $SrcTable in $SrcDatabase database has $Rows records."
        Write-Host "$SuccessPrefix $SuccessRowCheckMsg"
        SendSlackMsg "#applicationsupport_status" "$SuccessPrefix $SuccessRowCheckMsg" $TRUE 
    }
  } 
  Catch [System.Exception] 
  { 
    $ex = $_.Exception 
    $EventLogMsg = "$Table $SrcTable in $SrcDatabase database not exist or connection error." 
    Write-Host $ex.Message    
    PopSQLEventLog $SrcServer $SrcDatabase "$ErrorPrefix  $EventLogMsg"
    SendSlackMsg "#applicationsupport_status" "$ErrorPrefix  $EventLogMsg" $FALSE 
  } 
  Finally 
  { 
    $SrcConn.Close() 
    $SrcConn.Dispose()  
  }  
